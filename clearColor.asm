setupClearColor(red, green, blue, ptr) {
  bufferId = load(0x136E0C)
  
  store(0x136DD8 + bufferId * 16, red)
  store(0x136DD9 + bufferId * 16, green)
  store(0x136DDA + bufferId * 16, blue)
  
  store(0x136DDC + bufferId * 16, load(0x136E84 + bufferId * 2)) // Area Offset X Buffer
  store(0x136DDE + bufferId * 16, load(0x136E88 + bufferId * 2)) // Area Offset Y Buffer
  store(0x136DE2 + bufferId * 16, load(0x136F14))
  
  if(load(0x136E7D) != 0)
    store(0x136DE0 + bufferId * 16, load(0x136F10) * 1.2)
  else
    store(0x136DE0 + bufferId * 16, load(0x136F10))
  
  probSwapPointer(load(ptr + 0x10), 0x136DD4 + bufferId * 16)
}

