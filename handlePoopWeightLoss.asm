void handlePoopWeightLoss(digimonType) {
  weightLoss = (load(0x1225CC + digimonType * 28) + random(4)) / 4
  oldWeight = load(0x1384A2)
  
  newWeight = oldWeight - weightLoss
  
  store(0x1384A2, newWeight)
  if(newWeight < 1)
    store(0x1384A2, 1)
}

