    for(i = -2; i < 3; i++)
      if(isLinearPathBlocked(tileX1, tileY1 + i, tileX2, tileY2 + i) != 0)
        break
  }
  else {
    for(i = -2; i < 3; i++)
      if(isLinearPathBlocked(tileX1 + i, tileY1, tileX2 + i, tileY2) != 0)
        break
  }
  
  if(i == 3)
    return 0
  
  return 1
}

